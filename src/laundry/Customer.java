      public void tampilkan(){
         DefaultTableModel model = new DefaultTableModel();
         String cari = txtCari.getText();
         try {
             stat = con.createStatement();
             rs = stat.executeQuery("SELECT * FROM tb_customer WHERE id_customer LIKE '%"+cari+"%'");
             
             
             model.addColumn("Id Customer");
             model.addColumn("Nama Customer");
             model.addColumn("Nomor Telepon Customer");
             model.addColumn("Alamat Customer");
             
             model.getDataVector().removeAllElements();
             model.fireTableDataChanged();
             model.setRowCount(0);
             
             while (rs.next()) {
                 Object[] data = {
                  rs.getString("id_customer"),
                  rs.getString("nama_customer"),
                  rs.getString("no_telpon_customer"),
                  rs.getString("alamat_rumah"),
                 };
                 model.addRow(data);
                 tb_customer.setModel(model);
             }
         } catch (Exception e) {
         }
     
     }
      
      
      
     
