    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         try {
            stat = con.createStatement();
            //Notifikasi bila data kosong 
            if (txtIdCustomer.getText().equals("") ||
                    txtNamaCustomer.getText().equals("") ||
                    txtNoTelpoCustomer.getText().equals("") ||
                    txtAlamatCustomer.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong");                   
            }
            if (btnSave.getText() == "Save") {
                String cek = "SELECT * FROM tb_customer WHERE id_customer = '" + txtIdCustomer.getText() + "'";
                rs = stat.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "ID Customer Sudah Ada");
                }else{
                    String sql = "INSERT INTO tb_customer VALUES ('" + txtIdCustomer.getText() + 
                            "','" + txtNamaCustomer.getText() +
                            "','" + txtNoTelpoCustomer.getText() +
                            "','" + txtAlamatCustomer.getText() +"')";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Simpan Data Berhasil");
                    kosong();
                    tampilkan();
                }
            }
        } catch (Exception e) {
            kosong();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

